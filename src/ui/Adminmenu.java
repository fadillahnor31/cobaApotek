    private void load_profil_table(){
        // membuat tampilan model tabel
        
        DefaultTableModel profil_model = new DefaultTableModel();
        profil_model.addColumn("No");
        profil_model.addColumn("NIP");
        profil_model.addColumn("Password");
        profil_model.addColumn("Nama");
        profil_model.addColumn("Gender");
        profil_model.addColumn("Alamat");
        profil_model.addColumn("Status");
        
        //menampilkan data database kedalam tabel
        try {
            int no=1;
            String sql = "select * from karyawan";
            java.sql.Connection conn=(Connection)Config.ConfigDB();
            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet res=stm.executeQuery(sql);
            while(res.next()){
                profil_model.addRow(new Object[]{no++, res.getString(1),res.getString(2),res.getString(3),res.getString(4),res.getString(5),res.getString(6),});
            }
            datatable.setModel(profil_model);
        } catch (Exception e) {
        }
    }
    
