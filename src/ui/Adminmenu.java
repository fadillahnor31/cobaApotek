    private void datatableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_datatableMouseClicked
        // TODO add your handling code here:

        // Get data
        int row = datatable.rowAtPoint(evt.getPoint());

        String nip = datatable.getValueAt(row, 1).toString();
        String password = datatable.getValueAt(row,2).toString();
        String nama = datatable.getValueAt(row, 3).toString();
        String gender = datatable.getValueAt(row, 4).toString();
        String alamat = datatable.getValueAt(row, 5).toString();
        String status = datatable.getValueAt(row, 6).toString();

        // Set data to form
        txtnama.setText(nama);
        txtnip.setText(nip);
        if("laki-laki".equals(gender)){
            txtgender.getModel().setSelectedItem("Laki-Laki");
        } else {
            txtgender.getModel().setSelectedItem("Perempuan");
        }
        txtalamat.setText(alamat);
        
        if(null == status){
            txtstatus.getModel().setSelectedItem("dokter");
        } else switch (status) {
            case "admin":
                txtstatus.getModel().setSelectedItem("admin");
                break;
            case "petugas":
                txtstatus.getModel().setSelectedItem("petugas");
                break;
            default:
                txtstatus.getModel().setSelectedItem("dokter");
                break;
        }
        txtpassword.setText(password);
        
    }//GEN-LAST:event_datatableMouseClicked

