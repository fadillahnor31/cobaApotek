    private void load_obat_table() throws SQLException{
        // obat model
        DefaultTableModel obat_model = new DefaultTableModel();
        obat_model.addColumn("No");
        obat_model.addColumn("Kode");
        obat_model.addColumn("Nama");
        obat_model.addColumn("Jenis");
        obat_model.addColumn("Klasifikasi");
        obat_model.addColumn("Harga");
        obat_model.addColumn("Stok");
        obat_model.addColumn("SupplierId");
        
        // supplier modal
        DefaultTableModel supp_model = new DefaultTableModel();
        supp_model.addColumn("SupplierId");
        supp_model.addColumn("Nama");       
        
        //menampilkan data database kedalam tabel
        try {
            int no=1;
            String sql_obat = "select * from obat";
            java.sql.Connection conn=(Connection)Config.ConfigDB();
            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet res=stm.executeQuery(sql_obat);
            while(res.next()){
                obat_model.addRow(new Object[]{no++, res.getString(1),res.getString(2),res.getString(3),res.getString(4),res.getString(5),res.getString(6),res.getString(7)});
            }
            obattable.setModel(obat_model);
        } catch (Exception e) {
        } finally {
            String sql_supp = "select supplier_id, nama_supplier from supplier";
            java.sql.Connection conn=(Connection)Config.ConfigDB();
            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet res_supp=stm.executeQuery(sql_supp);
            
            while (res_supp.next()){
                supp_model.addRow(new Object[] {res_supp.getString(1), res_supp.getString(2)});
            }
            suppliertable.setModel(supp_model);
            
        }
    }
    
