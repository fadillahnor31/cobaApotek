    private void obattableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_obattableMouseClicked
        // TODO add your handling code here:
        
        // Get data
        int row = obattable.rowAtPoint(evt.getPoint());

        String kodeobat = obattable.getValueAt(row, 1).toString();
        String namaobat = obattable.getValueAt(row, 2).toString();
        String jenisobat = obattable.getValueAt(row,3).toString();
        String klasifikasiobat = obattable.getValueAt(row, 4).toString();
        String hargaobat = obattable.getValueAt(row, 5).toString();
        String stok = obattable.getValueAt(row, 6).toString();
        String supplierid = obattable.getValueAt(row, 7).toString();

        // Set data to form
        
        txtkodeobat.setText(kodeobat);
        txtnamaobat.setText(namaobat);
        switch(jenisobat){
            case "Cair":
                txtjenisobat.getModel().setSelectedItem("Cair");
                break;
            case "Tablet":
                txtjenisobat.getModel().setSelectedItem("Tablet");
                break;
            case "Kapsul":
                txtjenisobat.getModel().setSelectedItem("Kapsul");
                break;
            case "Oles":
                txtjenisobat.getModel().setSelectedItem("Oles");
                break;
            case "Tetes":
                txtjenisobat.getModel().setSelectedItem("Tetes");
                break;
            case "Suntik":
                txtjenisobat.getModel().setSelectedItem("Suntik");
                break;
            case "Lain-Lain":
                txtjenisobat.getModel().setSelectedItem("Lain-Lain");
                break;
        }
        txtklasifikasiobat.getModel().setSelectedItem(null);
        txtharga.setText(hargaobat);
        txtstok.setText(stok);
        txtsupplierid.setText(supplierid);
    }//GEN-LAST:event_obattableMouseClicked

